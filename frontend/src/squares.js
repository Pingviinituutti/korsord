export const createCoordGrid = () => {
  const grid = [];
  let idx = 0;
  squares.forEach((row) => {
    grid.push([]);
    row.forEach((_) => {
      grid[grid.length - 1].push(idx);
      idx += 1;
    });
  });
  return grid;
};

